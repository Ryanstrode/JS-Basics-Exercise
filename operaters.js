//Operators
//Adding and subtracting (+ for addition, - for subtraction)
console.log(2+2);
console.log(2.25 + 2.5 + 1.125);
console.log(3-1);

//Multiplying and dividing (* for multiplying, / for dividing)
console.log(4*5); // 20
console.log(1.5*2.5); // 3.75
console.log(1/4); // 0.25

//Exponentiation (** for exponentiation)
//Raises the first number to the power of the second number
console.log(5**3); // 125
//5*5*5 = 125

//Modulus (AKA REMAINDER)
//Returns the division remainder
//Percentage sign % is used ofr modulus
console.log(10%3); // 1
console.log(14%4); // 2
//Since 5 cannot go into 3, the remainder is 3. (NOTE: whenever the first number is less than the second, the first number will always be the remainder.)
